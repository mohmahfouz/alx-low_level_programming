#include "3-calc.h"

/**
* op_add - adds
* @a: number 
* @b: second number
* Return: result
*/

int op_add(int a, int b)
{
	return (a + b);
}



/**
* op_sub - subs
* @a: number
* @b: second number
* Return: result
*/

int op_sub(int a, int b)
{
	return (a - b);
}


/**
* op_mul - multiply
* @a: number
* @b: second number
* Return: result
*/

int op_mul(int a, int b)
{
	return (a * b);
}



/**
* op_div - division
* @a: numberk
* @b: second number
* Return: result
*/


int op_div(int a, int b)
{
	return (a / b);
}


/**
* op_mod - modulus
* @a: number
* @b: second number
* Return: result
*/

int op_mod(int a, int b)
{

	return (a % b);
}
